#!/usr/bin/env python3
"""Create comparison plots from BullshitBench evaluation summaries."""

from __future__ import annotations

import argparse
import csv
from pathlib import Path


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--summary-csv",
        type=Path,
        required=True,
        help="CSV generated by scripts/eval_bullshitbench_models.py",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=None,
        help="Directory for plot files. Defaults next to the CSV.",
    )
    return parser.parse_args()


def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open() as handle:
        return list(csv.DictReader(handle))


def maybe_float(value: str | None) -> float | None:
    if value in (None, "", "None"):
        return None
    return float(value)


def main() -> None:
    args = parse_args()
    rows = read_rows(args.summary_csv)
    if not rows:
        raise SystemExit(f"No rows found in {args.summary_csv}")

    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise SystemExit("matplotlib is required for plotting. Install it in this environment and rerun.") from exc

    output_dir = (args.output_dir or args.summary_csv.parent).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    names = [row["model_name"] for row in rows]
    nonsense_score = [maybe_float(row.get("nonsense_mean_combined")) for row in rows]
    nonsense_reject = [maybe_float(row.get("nonsense_rejection_rate")) for row in rows]
    salvaged_score = [maybe_float(row.get("salvaged_mean_combined")) for row in rows]
    salvaged_refusal = [maybe_float(row.get("salvaged_rejection_rate")) for row in rows]
    has_salvaged = any(value is not None for value in salvaged_score)

    if has_salvaged:
        fig, axes = plt.subplots(2, 2, figsize=(14, 9))
        axes = axes.flatten()
    else:
        fig, axes = plt.subplots(1, 2, figsize=(14, 4.5))
        axes = list(axes)

    panels = [
        ("Nonsense Mean Score", nonsense_score, "Combined judge score", axes[0], "#1f77b4"),
        ("Nonsense Rejection Rate", nonsense_reject, "Rejected share", axes[1], "#d62728"),
    ]
    if has_salvaged:
        panels.extend(
            [
                ("Salvaged Mean Score", salvaged_score, "Combined judge score", axes[2], "#2ca02c"),
                ("Salvaged Refusal Rate", salvaged_refusal, "Rejected share", axes[3], "#9467bd"),
            ]
        )

    for title, values, ylabel, axis, color in panels:
        clean_values = [0.0 if value is None else value for value in values]
        axis.bar(names, clean_values, color=color)
        axis.set_title(title)
        axis.set_ylabel(ylabel)
        axis.tick_params(axis="x", rotation=20)
        axis.set_ylim(0, 1)

    fig.suptitle("BullshitBench Comparison", fontsize=16)
    fig.tight_layout()

    png_path = output_dir / "bullshitbench_comparison.png"
    pdf_path = output_dir / "bullshitbench_comparison.pdf"
    fig.savefig(png_path, dpi=200, bbox_inches="tight")
    fig.savefig(pdf_path, bbox_inches="tight")
    print(f"[done] wrote {png_path}")
    print(f"[done] wrote {pdf_path}")


if __name__ == "__main__":
    main()
